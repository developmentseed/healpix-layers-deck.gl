/**
 * Regenerates test/gpu/shader-chunks.gen.mjs from src/shaders/*.glsl.ts exports.
 * Run from package root: node scripts/extract-readback-glsl.cjs
 */
const fs = require('fs');
const path = require('path');

const root = path.join(__dirname, '..');

const chunks = [
  ['src/shaders/int64.glsl.ts', 'INT64_GLSL'],
  ['src/shaders/fp64.glsl.ts', 'FP64_GLSL'],
  ['src/shaders/healpix-decompose.glsl.ts', 'HEALPIX_DECOMPOSE_GLSL'],
  ['src/shaders/healpix-corners.glsl.ts', 'HEALPIX_CORNERS_GLSL']
];

function extractGlslConst(tsPath, exportName) {
  const text = fs.readFileSync(path.join(root, tsPath), 'utf8');
  const marker = `export const ${exportName}: string = /* glsl */ \``;
  const i = text.indexOf(marker);
  if (i < 0) {
    throw new Error(`Missing ${exportName} in ${tsPath}`);
  }
  const start = i + marker.length;
  const end = text.indexOf('`', start);
  if (end < 0) {
    throw new Error(`Unterminated glsl template in ${tsPath}`);
  }
  return text.slice(start, end);
}

let out =
  '// AUTO-GENERATED by scripts/extract-readback-glsl.cjs — do not edit by hand.\n';
for (const [rel, name] of chunks) {
  const body = extractGlslConst(rel, name);
  out += `export const ${name} = ${JSON.stringify(body)};\n`;
}

const dest = path.join(root, 'test/gpu/shader-chunks.gen.mjs');
fs.mkdirSync(path.dirname(dest), { recursive: true });
fs.writeFileSync(dest, out);
console.log('Wrote', path.relative(root, dest));
